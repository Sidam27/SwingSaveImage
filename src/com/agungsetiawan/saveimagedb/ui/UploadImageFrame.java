    private void initTable(){
        jTableBook.getSelectionModel().addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent e) {
                try {
                    int row=jTableBook.getSelectedRow();
                    Book book=bookTableModel.findOne(row);
                    
                    Toolkit toolkit=Toolkit.getDefaultToolkit();
                    
                    String path=new File(".").getCanonicalPath();
                    Image image=toolkit.getImage(path+"/image/"+book.getImage());
    //                Image image=toolkit.getImage(getClass().getResource("/image/"+book.getImage()));
                    Image imagedResized=image.getScaledInstance(200, 250, Image.SCALE_DEFAULT);
                    ImageIcon icon=new ImageIcon(imagedResized);
                    jLabelGambar.setIcon(icon);
                } catch (IOException ex) {
                    Logger.getLogger(UploadImageFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        });
        
            bookService=SaveImageDB.getBookService();
            bookTableModel=new BookTableModel(bookService.findAll());
            jTableBook.setModel(bookTableModel);
    }

